    private void jbtnUlogujSeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnUlogujSeActionPerformed
        try {                                             
            String korisnickoIme = jtxtKorisnickoIme.getText().trim();
            String lozinka = jpfLozinka.getText().trim();
            String IPAdresa = jtxtIPAdresa.getText().trim();
            int port;
            try {
                port = Integer.parseInt(jtxtPort.getText().trim());
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Unesite broj u polje 'Port'");
                return;
            }
            
            konektujSe(IPAdresa, port);
            
            if (izvrsiValidaciju(korisnickoIme, lozinka) == true) {
                JFrame glavna = new FGlavna();
                glavna.setVisible(true);
                dispose();
                JOptionPane.showMessageDialog(glavna, "Uspesno ste se ulogovali");
            } else {
                JOptionPane.showMessageDialog(this, "Kombinacija korisnicko ime/lozinka je netacna");
                diskonektujSe();
            }
        } catch (Exception ex) {
//            Logger.getLogger(FRegistrovanjeReferenta.class.getName()).log(Level.SEVERE, null,ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jbtnUlogujSeActionPerformed

