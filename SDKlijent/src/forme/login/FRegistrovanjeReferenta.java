    public boolean izvrsiValidaciju(String korisnickoIme, String lozinka) {
        try {
            Referent referent = new Referent();
            referent.setKorisnickoIme(korisnickoIme);
            referent.setLozinka(lozinka);
            
            referent = Kontroler.getInstanca().ulogujReferenta(referent);
            Kontroler.getInstanca().postaviTrenutnogReferenta(referent);
            return true;
        } catch (Exception ex) {
//            Logger.getLogger(FRegistrovanjeReferenta.class.getName()).log(Level.SEVERE, null, ex);
            return false;
        }
    }

    private void konektujSe(String IPAdresa, int port) throws Exception {
        Klijent.getInstanca().poveziSe(IPAdresa, port);
    }

    private void diskonektujSe() {
        try {
            Klijent.getInstanca().getSocket().close();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Doslo je do ne predvidjene greske, prinudno se zatvara aplikacija");
            dispose();
            Logger.getLogger(FRegistrovanjeReferenta.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("Diskonektovao se");
    }
